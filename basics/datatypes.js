// JS is a dynamic language. let is a keyword to define anything and also we can change the value of it
// Initialising a number
let number = 5
// Initialising a String
let names = 'Java Script'
// Initialising a boolean
let bools = true
// Initialising a character
let initial = 'C'
// Printing all of them. use node filename.js to run the code in the terminal
console.log('number: '+number);
console.log('name: '+names);
console.log('boolean: '+bools);
console.log('char: '+initial);